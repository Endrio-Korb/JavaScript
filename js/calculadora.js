export default class Calculadora {

    constructor(){
        this.v1
        this.v2
    }
    
    adicao(){
        return this.v1 + this.v2
    }
    subtracao(){
        return this.v1 - this.v2
    }
    divicao(){
        return this.v1 / this.v2
    }
    multiplicacao(){
        return this.v1 * this.v2
    }

}